
const os = require('os');

// Prints the operating system platform
console.log(os.platform());

// Prints information about each logical CPU core, such as model and speed
console.log(os.cpus());

// Prints the home directory of the current user
console.log(os.homedir());

// Prints the hostname of the operating system
console.log(os.hostname());

// Prints the system uptime
console.log(os.uptime());

// Prints the total amount of system memory
console.log(os.totalmem());

// Prints the amount of free system memory
console.log(os.freemem());
